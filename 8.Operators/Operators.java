public class Operators {
    public static void main(String[] args) {
        // unary operators  ++  &  --
        // binary opeators + - * %

        int a = 10;
        int b = 0;

        b= a++;
        System.out.println(a);
        System.out.println(b);
        int c = 10;
        int d = 0;

        d= ++c;
        System.out.println(c);
        System.out.println(d);

        // Relational operators

        // ==  equal to 
        // != not equal to
        // >  greater than
        // < lesser than
        // >= greater than equal to
        // <= lesser than equal to


        // logical operators

        // &&  (logical AND)
        // || (logical OR)
        // !  (logical NOT)

        // Bitwise operators

        // & ( Binary AND)
        // | (Binary OR)
        // ^ (Binary XOR)
        // ~ (Binary One's complement)
        // << (Binary left shift)
        // >> (Binary right shift)
    }
}
